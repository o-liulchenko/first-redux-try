export const onlyDischarged = () => {
    const onlyDischarged = inpatientJournal.filter(item => item.medicalData.discharge === true);
    return onlyDischarged;
}

export const onlyInHospital = () => {
    const onlyInHospital = inpatientJournal.filter(item => item.medicalData.discharge === false);
    return onlyInHospital;
}

export const inpatientJournalFilter = (filter) => {
    switch (filter.by) {
        case 'discharge':
            const result = inpatientJournal.filter(item => item.medicalData.discharge === filter.value);
            return result;

        default:
            return inpatientJournal;
    }
}
